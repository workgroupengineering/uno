namespace Microsoft.UI.Xaml
{
	/// <summary>
	/// Specifies the display state of an element.
	/// </summary>
	public enum Visibility
	{
		/// <summary>
		/// Display the element.
		/// </summary>
		Visible,
		/// <summary>
		/// Do not display the element, and do not reserve space for it in layout.
		/// </summary>
		Collapsed,
	}
}

